#include<iostream>
using namespace std;
int main(){
    int p=10;
    int q;
    //using assign  value
    q=p;
    cout<<(q)<<endl;// value of  q=10
    //assign value using=+
    q+=p;
    cout<<(q)<<endl;// value of q=20
    // assign value using =*
    q*=p;
    cout<<(q)<<endl;// value of q=200
}