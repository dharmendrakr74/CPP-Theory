#include<iostream>
using namespace std;
int main(){
    //variable declaration
    int p=10,q=15;
    //== (equal to )operator
    cout<<(p==q)<<endl;//false
    //!= (not equal operator)
    cout<<(p!=q)<<endl;//true
    //>(greater than)operator
    cout<<(p>q)<<endl;//false
    //< (less than)operator
    cout<<(p<q)<<endl; //true
    // greater than equal (>=)operator
    cout<<(p>=q)<<endl;//false
    //less than equal operator
    cout<<(p<=q)<<endl;//true

}